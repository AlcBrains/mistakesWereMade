package com.christo;

public class TmpObject {
    int val;
    int pos;

    TmpObject() {
    }

    TmpObject(int val, int pos) {
        this.val = val;
        this.pos = pos;
    }

    public int getPos() {
        return pos;
    }

    public int getVal() {
        return val;
    }

}
